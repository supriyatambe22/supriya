package operator;

public class Incrementop {

	public static void main(String[] args) 
	{
		int a=2;
		 int b=3; 
		 System.out.println("Value of a "+a);
		 System.out.println("Value of b "+b);
		 
		 System.out.println("Value of ++a "+(++a));
		 System.out.println("Value of a "+a);
		 System.out.println("Value of b++ "+(b++));
		 System.out.println("Value of b "+b);
		}

	}

